class JobPosting {
  String employeeRole;
  String hotelName;
  String jobDescription;
  String location;
  String salary;
  bool status;
  String workingHours;

  JobPosting({
    required this.employeeRole,
    required this.hotelName,
    required this.jobDescription,
    required this.location,
    required this.salary,
    required this.status,
    required this.workingHours,
  });
}
